"""
Tipos de dados
str - string - textos 'Assim' "Assim"
int - inteiro - 123456 0 -1 -3
float - real/ponto flutuante - 10.50 1.99 0.01 -5.69
bool - booleano/lógico True False 10 == 10
"""

print('Luiz', type('Luiz'))
print(10, type(10))
print(25.6, type(25.6))
print(10 == 10, type(10 == 10))

# String: nome
print('Luiz Otávio', type('Luiz Otávio'))

# Int: idade
print(32, type(32))

# Float: altura
print(1.80, type(1.80))

# É maior de idade 10 > 20
print(32 > 18, type(32 > 18))
