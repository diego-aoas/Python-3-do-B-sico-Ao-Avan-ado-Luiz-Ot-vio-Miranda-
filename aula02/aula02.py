# 9. O comando print

print(123456)
print('Luiz', 'Otávio', 'Outra coisa')

# "sep" é uma função de separação/separador.
# "end" é a função para colocar algo no final do print, nesse caso é vazio fazendo com que junte os 2 prints.
print('Luiz', 'Otávio', sep='-', end='')
print('João', 'e', 'Maria', sep='-', end='')
